interface WebCompatibleIoniconsProps {
  name: keyof typeof Ionicons.glyphMap;
  size: number;
  color: string;
  style?: any;
}

const WebCompatibleIonicons: React.FC<WebCompatibleIoniconsProps> = ({ 
  name, 
  size, 
  color, 
  style 
}) => {
  // Use original Ionicons for native platforms
  if (!isWeb) {
    return <Ionicons name={name} size={size} color={color} style={style} />;
  }

  // For web, use SVG icons
  const iconPath = ICON_MAP[name];
  
  if (!iconPath) {
    // Fallback to original Ionicons if icon not found
    return <Ionicons name={name} size={size} color={color} style={style} />;
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={color}
      style={style}
      xmlns="http://www.w3.org/2000/svg"
    >
      <path d={iconPath} />
    </svg>
  );
};

